package omar;

public class OddOrEven {

    public static void main(String[] args) {

        OddOrEvenMethod(5);


    }

    public static void OddOrEvenMethod(int num) {
        if (num % 2 == 0) {
            System.out.println("Even");
        } else
            System.out.println("Odd");


    }

}
